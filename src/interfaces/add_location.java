    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jRadioLec.setActionCommand("Lecture Hall");
        jRadioButtonLab.setActionCommand("Laboratory");
        String build = jComboBox1.getSelectedItem().toString();
        try {
            // TODO add your handling code here:
            ResultSet rs = dbclass.search("SELECT buildId FROM building WHERE buildName = '"+build+"'");
            while (rs.next()) {
                int i = Integer.parseInt(rs.getString("buildId"));
                dbclass.iud("INSERT INTO location VALUES('"+jLabel7.getText()+"','"+jTextField2.getText()+"','"+buttonGroup1.getSelection().getActionCommand()+"','"+jTextField3.getText()+"','"+i+"')");
            }
            
            clearTextFiels();
            genLocID();
            JOptionPane.showMessageDialog(null, "Saved Succesfully");
        } catch (Exception ex) {
            Logger.getLogger(addLocation.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

