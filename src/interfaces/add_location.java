    private void genLocID() {
        try {
            ResultSet rs = dbclass.search("SELECT location_id as x FROM location order by location_id desc limit 1");
            if (rs.next()) {
                int i = Integer.parseInt(rs.getString("x"));
                ++i;
                jLabel7.setText(""+i);
            }
        } catch (Exception e) {
        }
    }

private void load_building(){
    try {
        ResultSet rs = dbclass.search("SELECT buildName FROM building");
        while (rs.next()) {            
            String building = rs.getString("buildName");
            jComboBox1.addItem(building);
        }
    } catch (Exception e) {
    }
}

private void genbuildId(){
    try {
        ResultSet rs = dbclass.search("SELECT count(buildId) as y FROM building ");
        if (rs.next()){
            int j = Integer.parseInt(rs.getString("y"));
                ++j;
                jLabel10.setText(""+j);
                        
                
        }
    } catch (Exception e) {
    }
}

